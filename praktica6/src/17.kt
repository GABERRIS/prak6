fun main() {
    // Определение массива целых чисел
    val numbers = intArrayOf(1, 2, 3, 4, 5)

    // Инициализация переменных для суммы и произведения
    var sum = 0
    var product = 1

    // Цикл по всем элементам массива
    for (number in numbers) {
        // Суммирование текущего элемента к сумме
        sum += number

        // Умножение текущего элемента на произведение
        product = number
    }

    // Вывод результата
    println("Сумма: $sum")
    println("Произведение: $product")
}